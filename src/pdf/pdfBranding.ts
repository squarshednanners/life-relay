import { rgb } from 'pdf-lib'
import type { PDFPage, PDFFont } from 'pdf-lib'

const BRAND_TEAL = rgb(0.06, 0.46, 0.43)

/**
 * Draws the Life Relay logo mark (relay baton with arcs) at the given position.
 * The mark is drawn at a fixed aspect ratio within the given size.
 * pdf-lib can't draw curves, so we approximate the arcs with line segments
 * and draw the baton as a thick angled line with rounded caps.
 */
export function drawLifeRelayMark(page: PDFPage, x: number, y: number, size: number) {
  const s = size / 512
  const white = rgb(1, 1, 1)

  // Background square
  page.drawRectangle({
    x,
    y: y - size,
    width: size,
    height: size,
    color: BRAND_TEAL,
  })

  // Left arc (giver) — approximated with line segments
  // Curve from (120,360) to (256,140) via bezier-like points
  const leftArcPoints = [
    { px: 120, py: 360 },
    { px: 120, py: 300 },
    { px: 130, py: 240 },
    { px: 155, py: 195 },
    { px: 190, py: 160 },
    { px: 225, py: 145 },
    { px: 256, py: 140 },
  ]
  for (let i = 0; i < leftArcPoints.length - 1; i++) {
    const a = leftArcPoints[i]
    const b = leftArcPoints[i + 1]
    page.drawLine({
      start: { x: x + a.px * s, y: y - size + (512 - a.py) * s },
      end:   { x: x + b.px * s, y: y - size + (512 - b.py) * s },
      thickness: 32 * s,
      color: white,
      opacity: 0.3,
    })
  }

  // Right arc (receiver) — approximated with line segments
  // Curve from (256,372) to (392,152)
  const rightArcPoints = [
    { px: 256, py: 372 },
    { px: 287, py: 372 },
    { px: 325, py: 357 },
    { px: 355, py: 320 },
    { px: 378, py: 265 },
    { px: 390, py: 205 },
    { px: 392, py: 152 },
  ]
  for (let i = 0; i < rightArcPoints.length - 1; i++) {
    const a = rightArcPoints[i]
    const b = rightArcPoints[i + 1]
    page.drawLine({
      start: { x: x + a.px * s, y: y - size + (512 - a.py) * s },
      end:   { x: x + b.px * s, y: y - size + (512 - b.py) * s },
      thickness: 32 * s,
      color: white,
      opacity: 0.3,
    })
  }

  // Baton — thick diagonal line (approximating rotated rounded rect)
  // From roughly (210,290) to (302,198) at -45 degrees
  page.drawLine({
    start: { x: x + 200 * s, y: y - size + (512 - 295) * s },
    end:   { x: x + 312 * s, y: y - size + (512 - 183) * s },
    thickness: 38 * s,
    color: white,
  })
}

/**
 * Draws the "Generated by Life Relay (liferelay.app)" branding line.
 */
export function drawGeneratedBy(
  page: PDFPage,
  font: PDFFont,
  pageWidth: number,
  y: number,
) {
  const text = 'Generated by Life Relay -- liferelay.app'
  const textWidth = font.widthOfTextAtSize(text, 7)
  page.drawText(text, {
    x: (pageWidth - textWidth) / 2,
    y,
    size: 7,
    font,
    color: rgb(0.45, 0.45, 0.45),
  })
}
